	/** must be invoked on the swing event thread */
	private void doUpdate(){
		charListModel.clear();
		for(int i = 0; i < characters.size(); i++){
			DaTextObject character = characters.getAsObject(i);
			charListModel.addElement((i+1)+": "+character.getText("name"));
		}
	}
