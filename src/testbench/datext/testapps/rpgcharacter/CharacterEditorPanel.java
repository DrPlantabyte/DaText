	private int calculatePointBuyValue(int... scores){
		int sum = 0;
		DaTextList lut = RPGCharacter.getInstance().getConfiguration().getObject("data").getList("ptbuy-LUT");
		for(int i = 0; i < scores.length; i++){
			int score = scores[i];
			sum += lut.getAsInt(score);
		}
		return sum;
	}
	
